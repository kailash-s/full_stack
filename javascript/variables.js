// variable is like a container where we can store values
let variable1 = 3;
console.log(variable1);

let calculation = 2+2;
console.log(calculation);
console.log(calculation+2);

let result = calculation+2;
console.log(result);

// semi-colons let js know that it's the end of an instruction
let message = 'hello';
console.log(message);

// we can re-assign a variable as below, no need for let again:
variable1 = 5;
console.log(variable1);

// we can use variables within itself to imcrement the value
variable1 = variable1+1;
console.log(variable1);

// we can also create variables in 2 other ways as below:
// const keeps the variable constant so it can't be changed later
const variable3 = 3;
// var used to be used instead of let but has some issues so not used anymore
var variable4 = 5;